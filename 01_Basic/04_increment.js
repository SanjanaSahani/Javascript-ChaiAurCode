// *****************Postfix Increment ***********************

let x = 3;
const y = x++;
console.log(x,y);
// Outpu: 4 3

let gameCounter = 100;
gameCounter++;
console.log(gameCounter);
// Outpu: 101

// ****************Prefix Increment ************************

let X = 3;
const Y = ++X;
console.log(X,Y);
// Outpu: 4 3

let gameCounter2 = 100;
++gameCounter2;
console.log(gameCounter2);
// Outpu: 101